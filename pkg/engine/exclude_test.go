func TestAridLineCache(t *testing.T) {
	ec := NewExclusionChecker(nil)

	// First call computes and caches
	result1 := ec.IsAridLine("test.go", 10, "log.Println(x)")
	// Second call should return cached value
	result2 := ec.IsAridLine("test.go", 10, "log.Println(x)")

	if result1 != result2 {
		t.Error("cached result should match")
	}
	if !result1 {
		t.Error("log.Println should be arid")
	}
}

func TestContainsPathSegment(t *testing.T) {
	tests := []struct {
		path    string
		segment string
		want    bool
	}{
		{"/project/vendor/pkg/foo.go", "vendor", true},
		{"/project/pkg/foo.go", "vendor", false},
		{"vendor/foo.go", "vendor", true},
		{"/project/myvendor/foo.go", "vendor", false}, // "myvendor" != "vendor"
		{"/project/testdata/foo.go", "testdata", true},
	}
	for _, tt := range tests {
		got := containsPathSegment(tt.path, tt.segment)
		if got != tt.want {
			t.Errorf("containsPathSegment(%q, %q) = %v, want %v", tt.path, tt.segment, got, tt.want)
		}
	}
}

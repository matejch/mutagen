func TestExcludeGeneratedHeader(t *testing.T) {
	ec := NewExclusionChecker(nil)
	dir := t.TempDir()

	// File with generation marker
	genFile := filepath.Join(dir, "gen.go")
	if err := os.WriteFile(genFile, []byte("// Code generated by foo. DO NOT EDIT.\npackage p\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	if !ec.ShouldExcludeFile(genFile) {
		t.Error("should exclude file with Code generated header")
	}

	// File without marker
	normalFile := filepath.Join(dir, "normal.go")
	if err := os.WriteFile(normalFile, []byte("package p\nfunc f() {}\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	if ec.ShouldExcludeFile(normalFile) {
		t.Error("should not exclude normal file")
	}
}

func TestExcludeUserPatterns(t *testing.T) {
	ec := NewExclusionChecker([]string{"*_mock.go", "internal_*.go"})

	if !ec.ShouldExcludeFile("/project/foo_mock.go") {
		t.Error("should exclude user pattern *_mock.go")
	}
	if !ec.ShouldExcludeFile("/project/internal_helpers.go") {
		t.Error("should exclude user pattern internal_*.go")
	}
	if ec.ShouldExcludeFile("/project/real.go") {
		t.Error("should not exclude non-matching file")
	}
}

func TestAridLines(t *testing.T) {
	arid := []string{
		"log.Printf(\"something\")",
		"defer f.Close()",
		"// this is a comment",
		"fmt.Println(err)",
		"slog.Info(\"msg\")",
		"panic(\"unreachable\")",
		"",
		"{",
		"}",
		"// mutagen:skip",
	}
	for i, line := range arid {
		ec := NewExclusionChecker(nil)
		if !ec.IsAridLine("test.go", i+1, line) {
			t.Errorf("should be arid: %q", line)
		}
	}

	notArid := []string{
		"if x > 0 {",
		"return a + b",
		"result := compute()",
		"for i := 0; i < n; i++ {",
		"x = y * z",
	}
	for i, line := range notArid {
		ec := NewExclusionChecker(nil)
		if ec.IsAridLine("test.go", i+1, line) {
			t.Errorf("should NOT be arid: %q", line)
		}
	}
}

